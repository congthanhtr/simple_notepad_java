    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        // TODO add your handling code here:
        String mssv = memmssvTxt.getText();
        DateTimeFormatter dtf = DateTimeFormatter.ofPattern("yyyy/MM/dd HH:mm:ss");  
        LocalDateTime now = LocalDateTime.now();  
        String return_date = now.toString();
        String name = memnameLab.getText();
        String book = bookLab.getText().replace(","," ");
        
        try {
            pst = conn.prepareStatement("Insert into returnbook(mssv, member_name, book_name, return_date) values(?,?,?,?)");
            pst.setString(1, mssv);
            pst.setString(2, name);
            pst.setString(3, book);
            pst.setString(4, return_date);
            
            int k = pst.executeUpdate();
            if (k == 1) {
                JOptionPane.showMessageDialog(this, "Return book created successfully!");
                memmssvTxt.setText("");
            }
            else {
                JOptionPane.showMessageDialog(this, "Return book created failed!");
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(Member.class.getName()).log(Level.SEVERE, null, ex);
        }
        return_load();
    }//GEN-LAST:event_saveButtonActionPerformed

    private void memmssvTxtKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_memmssvTxtKeyPressed
        // TODO add your handling code here:
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            String mssv = memmssvTxt.getText();
            
            try {
                pst = conn.prepareStatement("select * from member where mssv = ?");
                pst.setString(1, mssv);
                rs = pst.executeQuery();
                
                if (rs.next() == false) {
                    JOptionPane.showMessageDialog(this, "Member not found!");
                }
                else {
                    String member_name = rs.getString("member_name");
                    memnameLab.setText(member_name.trim());
                }
                
                pst = conn.prepareStatement("select * from issuebook where mssv = ?");
                pst.setString(1, mssv);
                rs = pst.executeQuery();
                
                if (rs.next() == false) {
                    bookLab.setText("not lending");
                }
                else {
                    String book_name = rs.getString("book_name")+", ";
                    while (rs.next()) {
                        book_name += rs.getString("book_name")+", ";
                    }
                    bookLab.setText(book_name);
                }
            } catch (SQLException ex) {
                Logger.getLogger(Return.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_memmssvTxtKeyPressed

    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        // TODO add your handling code here:
        DefaultTableModel d1 = (DefaultTableModel)returnTable.getModel();
        int selectedIndex = returnTable.getSelectedRow();
        int id = Integer.parseInt(d1.getValueAt(selectedIndex, 0).toString());
        try {
            pst = conn.prepareStatement("Delete from returnbook where id = ?");
            pst.setInt(1, id);
            
            int k = pst.executeUpdate();
            if (k == 1) {
                JOptionPane.showMessageDialog(this, "Return book deleted!");
                memnameLab.setText("");
                memmssvTxt.setText("");
                bookLab.setText("");
            }
            else {
                JOptionPane.showMessageDialog(this, "Deleted from Issue failed!");
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(Member.class.getName()).log(Level.SEVERE, null, ex);
        }
        saveButton.setEnabled(true);
        return_load();;
    }//GEN-LAST:event_deleteButtonActionPerformed

