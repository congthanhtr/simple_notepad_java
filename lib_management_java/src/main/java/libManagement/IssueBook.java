    public void issue_load() {
        int c;
        try {
            pst = conn.prepareStatement("Select * from issuebook");
            rs = pst.executeQuery();
            
            ResultSetMetaData rsmd = rs.getMetaData();
            c = rsmd.getColumnCount();
            
            DefaultTableModel d = (DefaultTableModel)issueTable.getModel();
            d.setRowCount(0);
            
            while (rs.next()) {
                Vector vt = new Vector();
                
                for (int i = 0; i < c; i++) {
                    vt.add(rs.getString("id"));
                    vt.add(rs.getString("mssv"));
                    vt.add(rs.getString("member_name"));
                    vt.add(rs.getString("book_name"));
                }
                
                d.addRow(vt);
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(Member.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    public void load_book_items() {
        try {
            pst = conn.prepareStatement("select book_name from book");
            rs = pst.executeQuery();
            while (rs.next()) {
                bookComboBox.addItem(rs.getString(1));
            }
        } catch (SQLException ex) {
            Logger.getLogger(IssueBook.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

