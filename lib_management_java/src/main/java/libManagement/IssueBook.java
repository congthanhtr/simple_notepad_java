    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        // TODO add your handling code here:
        String mssv = memmssvTxt.getText();
        String name = memnameTxt.getText();
        String book = bookComboBox.getSelectedItem().toString();
        
        try {
            pst = conn.prepareStatement("Insert into issuebook(mssv, member_name, book_name) values(?,?,?)");
            pst.setString(1, mssv);
            pst.setString(2, name);
            pst.setString(3, book);
            
            int k = pst.executeUpdate();
            if (k == 1) {
                JOptionPane.showMessageDialog(this, "Issue created successfully!");
                memnameTxt.setText("");
                memmssvTxt.setText("");
            }
            else {
                JOptionPane.showMessageDialog(this, "Issue created failed!");
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(Member.class.getName()).log(Level.SEVERE, null, ex);
        }
        issue_load();
    }//GEN-LAST:event_saveButtonActionPerformed

    private void memmssvTxtKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_memmssvTxtKeyPressed
        // TODO add your handling code here:
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            String mssv = memmssvTxt.getText();
            
            try {
                pst = conn.prepareStatement("select * from member where mssv = ?");
                pst.setString(1, mssv);
                rs = pst.executeQuery();
                
                if (rs.next() == false) {
                    JOptionPane.showMessageDialog(this, "Member not found!");
                }
                else {
                    String member_name = rs.getString("member_name");
                    memnameTxt.setText(member_name.trim());
                }
            } catch (SQLException ex) {
                Logger.getLogger(IssueBook.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_memmssvTxtKeyPressed

    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
        // TODO add your handling code here:
        String membername = memnameTxt.getText();
        String membermssv = memmssvTxt.getText();
        String book = bookComboBox.getSelectedItem().toString();
        DefaultTableModel d1 = (DefaultTableModel)memberTable.getModel();
        int selectedIndex = issueTable.getSelectedRow();
        int id = Integer.parseInt(d1.getValueAt(selectedIndex, 0).toString());
        try {
            pst = conn.prepareStatement("Update member set member_name = ?, mssv = ? book_name = ? where id = ?");
            pst.setString(1, membername);
            pst.setString(2, membermssv);
            pst.setString(3, book);
            pst.setInt(4, id);
            
            int k = pst.executeUpdate();
            if (k == 1) {
                JOptionPane.showMessageDialog(this, "Issue updated successfully!");
                memnameTxt.setText("");
                memmssvTxt.setText("");
                bookComboBox.setSelectedItem(-1);
                saveButton.setEnabled(true);
            }
            else {
                JOptionPane.showMessageDialog(this, "Issue updated failed!");
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(Member.class.getName()).log(Level.SEVERE, null, ex);
        }
        issue_load();
    }//GEN-LAST:event_updateButtonActionPerformed

